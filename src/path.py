class PathDefine:
    # xlsx for input.
    @staticmethod
    def getxlsx():
        return "xlsx/"

    # html for raw data.
    @staticmethod
    def gethtml():
        return "html/"

    # imgs for cache.
    @staticmethod
    def getimgs():
        return "img/"

    # docx for output.
    @staticmethod
    def getdocx():
        return "docx/"
